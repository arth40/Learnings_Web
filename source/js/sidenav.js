function openNav() {
  document.getElementById("sidenavid").style.width = "25%";
  document.getElementById("navbarid").style.marginLeft = "27%";
  document.getElementById("navbarid").style.width = "73%";
}

function closeNav() {
  document.getElementById("sidenavid").style.width = "0";
  document.getElementById("navbarid").style.width = "100%";
  document.getElementById("navbarid").style.marginLeft= "0"; 
}